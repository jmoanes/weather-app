// Current language
let currentLanguage = 'en';

// Language switcher functions
export function getCurrentLanguage() {
  return currentLanguage;
}

export function setLanguage(lang) {
  if (languages[lang]) {
    currentLanguage = lang;
    localStorage.setItem('weatherAppLanguage', lang);
    updateUILanguage();
  }
}

export function getTranslation(key) {
  const lang = languages[currentLanguage];
  return lang ? lang.translations[key] || key : key;
}

export function getLanguageName(langCode) {
  return languages[langCode] ? languages[langCode].name : langCode;
}

export function getLanguageFlag(langCode) {
  return languages[langCode] ? languages[langCode].flag : '🌐';
}

// Initialize language from localStorage
export function initLanguage() {
  const savedLang = localStorage.getItem('weatherAppLanguage');
  if (savedLang && languages[savedLang]) {
    currentLanguage = savedLang;
  } else {
    // Auto-detect language from browser
    const browserLang = navigator.language.split('-')[0];
    if (languages[browserLang]) {
      currentLanguage = browserLang;
    }
  }
}

// Update UI language
function updateUILanguage() {
  // Trigger a custom event to notify components
  window.dispatchEvent(new CustomEvent('languageChanged', {
    detail: { language: currentLanguage }
  }));
}

// Get all available languages
export function getAvailableLanguages() {
  return Object.keys(languages);
}
